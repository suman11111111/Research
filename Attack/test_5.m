clc,clear,close all;
N=10000;
dt=0.001;
E1=[1 0;0 1];
E2=0;
A11=[-5 -0.5;-0.5 -6.5];
A12=[-0.8;2.6];
A21=[-0.8 2.6];
A22=[-1.5];
A=[A11,A12;A21,A22];
B1=[1;2];
B2=[-1];
C1=[4 4;2 6];
C2=[0;-2];
x1=[1 0.5 -0.8;1 2 4.9];
x2=[-2 2.5 1.4];
L=[1 -1 0;0 1 -1;0 -2 0];
y=C1*x1+C2*x2;

for i=1:N
    u=-L*y';
    x1=x1+dt*inv(E1)*(A11*x1+A12*x2+B1*u);
    x2=-inv(A22)*(A21*x1+B2*u);
    y=C1*x1+C2*x2;
end

